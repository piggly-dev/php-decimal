use PHPUnit\Framework\TestCase;
use Piggly\Decimal\Decimal;
use Piggly\Decimal\DecimalConfig;

class CeilMethodDecimalTest extends TestCase
{
	/**
	 * Configuration
	 *
	 * @var DecimalConfig
	 */
	protected $_config;

	protected function setUp () : void
	{
		$this->_config = DecimalConfig::clone([
			'precision' => 20,
			'rounding' => 4,
			'toExpNeg' => -100,
			'toExpPos' => 100,
