	 * @param Decimal|integer|float|string $n
	 * @param integer $pr Precision
	 * @param integer $rm Rounding mode
	 * @param string $expected
	 * @return void
	 */
	public function testSetOne (
		$n,
		int $pr,
		int $rm,
		string $expected
	)
	{ 
		DecimalConfig::instance()->set(['precision'=>$pr, 'rounding'=>$rm]);
